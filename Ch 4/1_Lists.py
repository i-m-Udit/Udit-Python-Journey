# Lists are a container which is used to store some values
# Lists are mutable
# Lists can be changed
# Lists can be indexed as strings 
# Lists are always written in [] brackets 

# Lists methods:
L1=["Udit","Rohan",7,True,"Hello"] #For example  
L1.sort() #Sorts the list either in aphabetical order or numerical order
L1.reverse() #Reverses the current list
L1.append(7) #Adds 7 at the last
L1.insert(3,8) #Inserts 8 at the 3 index
L1.pop("Hello") #Removes the given value and returns its value
L1.remove("Rohan") #Removes the given value from the list
